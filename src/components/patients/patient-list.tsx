import { memo } from "react";
import { Space, Table, Tag, type TableProps } from "antd";
import { CustomButton } from "../common";
import { PiPencilSimple as EditIcon, PiTrash as DeleteIcon } from "react-icons/pi";

type Props = {};

interface DataType {
  key: string;
  name: string;
  age: number;
  address: string;
  tags: string[];
}

const columns: TableProps<DataType>["columns"] = [
  {
    title: "Name",
    dataIndex: "name",
    key: "name",
    render: (text) => <a>{text}</a>,
  },
  {
    title: "Age",
    dataIndex: "age",
    key: "age",
  },
  {
    title: "Address",
    dataIndex: "address",
    key: "address",
  },
  {
    title: "Tags",
    key: "tags",
    dataIndex: "tags",
    render: (_, { tags }) => (
      <>
        {tags.map((tag) => {
          return <Tag key={tag}>{tag.toUpperCase()}</Tag>;
        })}
      </>
    ),
  },
  {
    title: "Action",
    key: "action",
    render: (_, record) => (
      <Space size="middle">
        <CustomButton variant="neutral-text" icon={EditIcon} />
        <CustomButton variant="neutral-text" icon={DeleteIcon} />
      </Space>
    ),
  },
];
const data: DataType[] = [
  {
    key: "1",
    name: "John Brown",
    age: 32,
    address: "New York No. 1 Lake Park",
    tags: ["nice", "developer"],
  },
  {
    key: "2",
    name: "Jim Green",
    age: 42,
    address: "London No. 1 Lake Park",
    tags: ["loser"],
  },
  {
    key: "3",
    name: "Joe Black",
    age: 32,
    address: "Sydney No. 1 Lake Park",
    tags: ["cool", "teacher"],
  },
];

const Component = (props: Props) => {
  return <Table rowClassName="cursor-pointer" columns={columns} dataSource={data} />;
};

export const PatientList = memo(Component);
